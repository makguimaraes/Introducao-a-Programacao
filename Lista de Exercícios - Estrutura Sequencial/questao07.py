num1 = int(input('Digite um número inteiro: '))
num2 = int(input('Digite outro número inteiro: '))
num3 = float(input('Digite um número real: '))

res1 = (2 * num1) * (num2 / 2)
res2 = (3 * num1) + num3
res3 = num3 ** 3

print(f'O resultado da primeira operação é {res1}.')
print(f'O resultado da segunda operação é {res2}.')
print(f'O resultado da terceira operação é {res3}.')
